// Ïù¥ÎØ∏ÏßÄ ÏóÖÎ°úÎìú ÏÑ§Ï†ï
const fs = require('fs');

// uploads ÎîîÎ†âÌÜ†Î¶¨ ÏÉùÏÑ± (ÏóÜÏúºÎ©¥ ÏÉùÏÑ±)
const uploadsDir = 'uploads/recruitments/';
if (!fs.existsSync(uploadsDir)) {
  fs.mkdirSync(uploadsDir, { recursive: true });
}

const storage = multer.diskStorage({
  destination: function (req, file, cb) {
    cb(null, uploadsDir)
  },
  filename: function (req, file, cb) {
    const uniqueSuffix = Date.now() + '-' + Math.round(Math.random() * 1E9);
    cb(null, 'recruitment-' + uniqueSuffix + path.extname(file.originalname));
  }
});

const upload = multer({ 
  storage: storage,
  limits: {
    fileSize: 5 * 1024 * 1024 // 5MB
  },
  fileFilter: function (req, file, cb) {
    if (file.mimetype.startsWith('image/')) {
      cb(null, true);
    } else {
      cb(new Error('Ïù¥ÎØ∏ÏßÄ ÌååÏùºÎßå ÏóÖÎ°úÎìú Í∞ÄÎä•Ìï©ÎãàÎã§.'), false);
    }
  }
});

// Î™®Îì† Î™®ÏßëÍ≥µÍ≥† Ï°∞Ìöå
router.get('/', async (req, res) => {
  try {
    const {
      page = 1,
      limit = 12,
      category,
      projectType,
      location,
      experience,
      paymentType,
      status = 'Î™®ÏßëÏ§ë',
      search,
      sortBy = 'createdAt',
      sortOrder = 'desc'
    } = req.query;

    // ÌïÑÌÑ∞ Ï°∞Í±¥ Íµ¨ÏÑ±
    const filter = {};
    
    if (status && status !== 'all') filter.status = status;
    if (category && category !== 'all') filter.category = category;
    if (projectType && projectType !== 'all') filter.projectType = projectType;
    if (location && location !== 'all') filter.location = location;
    if (experience && experience !== 'all') filter.experience = experience;
    if (paymentType && paymentType !== 'all') filter['payment.type'] = paymentType;

    // Í≤ÄÏÉâ Ï°∞Í±¥
    if (search) {
      filter.$or = [
        { title: { $regex: search, $options: 'i' } },
        { content: { $regex: search, $options: 'i' } },
        { tags: { $in: [new RegExp(search, 'i')] } }
      ];
    }

    // Ï†ïÎ†¨ Ï°∞Í±¥
    const sort = {};
    if (sortBy === 'deadline') {
      sort.applicationDeadline = sortOrder === 'desc' ? -1 : 1;
    } else if (sortBy === 'urgent') {
      sort.isUrgent = -1;
      sort.createdAt = -1;
    } else {
      sort[sortBy] = sortOrder === 'desc' ? -1 : 1;
    }

    const recruitments = await ActorRecruitment.find(filter)
      .populate('userId', 'email')
      .sort(sort)
      .limit(limit * 1)
      .skip((page - 1) * limit)
      .lean();

    const total = await ActorRecruitment.countDocuments(filter);

    res.json({
      success: true,
      data: recruitments,
      pagination: {
        page: parseInt(page),
        limit: parseInt(limit),
        total,
        pages: Math.ceil(total / limit)
      }
    });
  } catch (error) {
    console.error('Î™®ÏßëÍ≥µÍ≥† Ï°∞Ìöå Ïò§Î•ò:', error);
    res.status(500).json({ 
      success: false, 
      message: 'ÏÑúÎ≤Ñ Ïò§Î•òÍ∞Ä Î∞úÏÉùÌñàÏäµÎãàÎã§.' 
    });
  }
});

